function gridshow(mpc)
g1=digraph(mpc.branch(:,1),mpc.branch(:,2));
figure1 = figure;
axes1 = axes('Parent',figure1);
hold(axes1,'on');
h=plot(g1,'Parent',axes1,'MarkerSize',2,...
    'NodeColor',[0.63921568627451 0.07843137254902 0.180392156862745],...
    'ShowArrows','on',...
    'ArrowSize',4,...
    'EdgeColor',[0.470588235294118 0.670588235294118 0.188235294117647],...
    'EdgeLabel',{},...
    'NodeLabel',{},...
    'Layout',...
    'subspace');
labelnode(h,mpc.branch(:,1),cellstr(num2str(mpc.branch(:,1))))
title('IEEE Test System');
box(axes1,'on');
set(axes1,'XTick',zeros(1,0),'YTick',zeros(1,0),'YTickLabel',{});
